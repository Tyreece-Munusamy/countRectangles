package com.company;

import java.util.*;

public class PlottedPoint {
    private final int x;
    private final int y;

    public PlottedPoint(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
